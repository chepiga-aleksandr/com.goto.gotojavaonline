package module5.module5V2;

public class MinMaxPair {

    public int min, max;

    public MinMaxPair(int minimum, int maximum) {
        this.min = min;
        this.max = max;
    }

    @Override
    public String toString() {
        return "Максимальный элемент массива: " + max + ", Минимальный элемент массива  " + min;
    }
}
